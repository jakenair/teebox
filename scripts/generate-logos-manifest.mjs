#!/usr/bin/env node
// Scan assets/logos/*.png and write a manifest of available slugs.
// dailySeed() filters the course pool by this set so the Bingo board
// only ever shows courses that have a real PNG logo — no emoji fallbacks.

import fs from 'node:fs';
import path from 'node:path';

const ROOT = '/Users/jakenair/Desktop/teebox';
const LOGOS_DIR = path.join(ROOT, 'assets/logos');
const OUT = path.join(LOGOS_DIR, 'manifest.js');

const slugs = fs
  .readdirSync(LOGOS_DIR)
  .filter(f => f.endsWith('.png'))
  .map(f => f.replace(/\.png$/, ''))
  .sort();

const lines = [
  '// Auto-generated by scripts/generate-logos-manifest.mjs.',
  '// Lists course slugs with a real PNG logo in /assets/logos/.',
  '// dailySeed() filters Bingo course pool by this set — no emoji fallbacks.',
  '',
  'export const LOGOS_AVAILABLE = new Set([',
  ...slugs.map(s => `  '${s}',`),
  ']);',
  '',
];

fs.writeFileSync(OUT, lines.join('\n'));
console.log(`Wrote ${slugs.length} slugs to ${OUT}`);
